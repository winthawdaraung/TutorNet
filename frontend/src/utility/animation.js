export const slideRight = (delay = 0) => ({
  // ซ่อนองค์ประกอบ - สถานะเริ่มต้น
  hidden: {
    opacity: 0,
    x: -100,
  },
  // แสดงองค์ประกอบ - สถานะสุดท้าย
  visible: {
    opacity: 1,
    x: 0,
    transition: {
      type: "spring", // ใช้แบบสปริงเพื่อการเคลื่อนไหวที่เป็นธรรมชาติ
      duration: 0.5, // ระยะเวลาในการเคลื่อนไหว (วินาที)
      delay: delay, // หน่วงเวลาก่อนเริ่มเคลื่อนไหว
      stiffness: 50, // ค่าความแข็งของสปริง (ยิ่งมากยิ่งแข็ง)
      damping: 12, // ค่าความหน่วงของการเด้ง (ยิ่งมากยิ่งเด้งน้อย)
    },
  },
});
